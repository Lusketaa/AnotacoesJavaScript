//Operadores Aritméticos em JavaScript

// + (Operador de soma, segue a mesma regra da matemática normal)

2 + 2 == 4

// * (Multiplicador)

2 * 2 ==4

// ** (Potência)

3 ** 3 == 27 // (3 ao cubo)
3 ** 2 ==9 // (3 ao quadrado)

// / (Divisor)

27 / 3 == 9

// % (Módulo, é o resto da divisão)

6 % 4 == 2 // (6 dividido por 4 dá 1, e sobra 2)